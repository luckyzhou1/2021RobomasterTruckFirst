/**
  * @brief This function handles USART3 global interrupt.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */

  /* USER CODE END USART3_IRQn 0 */
  HAL_UART_IRQHandler(&huart3);
  /* USER CODE BEGIN USART3_IRQn 1 */
    uint32_t buff_length;//接收数据长度
	if(__HAL_UART_GET_FLAG(&huart3,UART_FLAG_IDLE))//检测到DMA线路空闲
	{
		
		HAL_UART_DMAStop(&huart3);//关闭DMA接收
		__HAL_UART_CLEAR_IDLEFLAG(&huart3);//通过读取SR,DR寄存器去清零,使其退出串Usart5_Clean_IDLE_Flag口中断。
		buff_length = RC_BUFFER_SIZE - (__HAL_DMA_GET_COUNTER(&hdma_usart3_rx));//读取DMA剩余传输量
		USART3_RX_Lenth = buff_length;//获取数据长度
        vision_connect(Rx_Buffer);//视觉数据接收
//		printf("%f\r\n", VisonRecvData.visionYawData);
//		printf("%d\r\n", VisonRecvData.model);
		memset(Rx_Buffer,0,100);
		HAL_UART_Receive_DMA(&huart3,Rx_Buffer,RC_BUFFER_SIZE);//重启DMA接收
    }
  /* USER CODE END USART3_IRQn 1 */
}

